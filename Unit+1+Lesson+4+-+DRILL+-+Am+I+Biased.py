# <h3>You're testing advertising emails for a bathing suit company and you test one version of the email in February and the other in May.</h3>
# 
# One obvious one is the timing when the latest one is closer to summer than the one in February. Another issue might be that the group receiving emails can be different from each other. Even the group can be randomly selected and still representative for the population the observer of the result may conclude differently in May compared to February.
# Weather can also play an impact for example if one group is exposed to winter storm and similar.
# 
# <h3>You open a clinic to treat anxiety and find that the people who visit show a higher rate of anxiety than the general population.</h3>
# 
# This is as obvious as going to bank to borrow money.
# How one can measure the level of anxiety in the general population? The term general population is very vague and can be interpreted differently. Also, it is questionable if it can be measured meaningfully. Also, the word anxiety is very loose.
# 
