# <h3>You launch a new ad billboard based campaign and see an increase in website visits in the first week.</h3>
# 
# The first question is to know if the website address was mentioned on the billboard. Also, location of billboard should be matched with locations of new visitors, if possible.
# Can the increase in visit numbers be associated with other external factors. Could be the competitor national media coverage triggered an industry wide interest in the product or service.
# Why increase in first week if we assume billboard ad usually last longer than one week. This could actually be the actual trigger of extra visit if for example one is in small city with limited population and diminished freshness of the ad.
# 
# <h3>You launch a loyalty program but see no change in visits in the first week.</h3>
# 
# Loyalty is more about keeping the existing client group and new visitors. Existing clients may not need to purchase more because of this program but instead have less tendency to switch. However, new comers may become motivated to switch to here from other place if this is attractive. 
# Also, the loyalty program may have some cumulative mechanism that become attractive after some repeat visits hence its effect might be lagged.
# Or it could be the competitor stopped their loyalty program or shut down and this business just got new clients without help of the new royalty program.
